#!/usr/bin/env node

// Patches the upstream vercel/workflow workbench app for Restate compatibility.
// Two operations:
//   1. Adds Turbopack resolveAlias entries to next.config.ts
//   2. Creates a .restate-well-known catch-all route for Restate service discovery

const fs = require("fs");
const path = require("path");

const appDir = process.argv[2];
if (!appDir) {
  console.error(
    "Usage: node patch-upstream-workbench.cjs <path-to-workbench-app>"
  );
  process.exit(1);
}

// ---------------------------------------------------------------------------
// 1. Patch next.config.ts with Turbopack resolveAlias
// ---------------------------------------------------------------------------

const configPath = path.join(appDir, "next.config.ts");
if (!fs.existsSync(configPath)) {
  console.error(`ERROR: ${configPath} not found`);
  process.exit(1);
}

let config = fs.readFileSync(configPath, "utf8");

const RESOLVE_ALIAS = `
    resolveAlias: {
      "workflow/api": "@restatedev/workflow/api",
      "workflow/runtime": "@restatedev/workflow/runtime",
      "workflow/internal/private": "@restatedev/workflow/internal/private",
    },`;

// Strategy 1: turbopack block exists with properties — insert resolveAlias
// Match "turbopack: {" then any content up to a closing "}" at the same level
if (/turbopack:\s*\{/.test(config) && !config.includes("resolveAlias")) {
  // Find the turbopack opening brace and insert after the first property line
  config = config.replace(
    /(turbopack:\s*\{[^\n]*\n)/,
    `$1${RESOLVE_ALIAS}\n`
  );
  console.log("Patched next.config.ts: inserted resolveAlias into existing turbopack block");
} else if (!config.includes("resolveAlias")) {
  // Strategy 2: no turbopack block — add one to the config object
  const configObjPattern = /(const\s+\w+\s*(?::\s*\w+)?\s*=\s*\{)/;
  if (configObjPattern.test(config)) {
    config = config.replace(
      configObjPattern,
      `$1\n  turbopack: {${RESOLVE_ALIAS}\n  },`
    );
    console.log("Patched next.config.ts: added turbopack block with resolveAlias");
  } else {
    console.error("WARNING: Could not find config object to patch in next.config.ts");
  }
} else {
  console.log("next.config.ts already contains resolveAlias — skipping");
}

fs.writeFileSync(configPath, config);

// Validate the patch
if (!config.includes("@restatedev/workflow/api")) {
  console.error("ERROR: Patch validation failed — resolveAlias not found in patched config");
  process.exit(1);
}

// ---------------------------------------------------------------------------
// 2. Create .restate-well-known catch-all route
// ---------------------------------------------------------------------------

// Find the app directory (Next.js App Router)
const candidates = [
  path.join(appDir, "app"),
  path.join(appDir, "src", "app"),
];
const appRouterDir = candidates.find((d) => fs.existsSync(d));

if (!appRouterDir) {
  console.error(
    `WARNING: Could not find app/ or src/app/ directory in ${appDir} — skipping route injection`
  );
} else {
  const routeDir = path.join(
    appRouterDir,
    ".restate-well-known",
    "[[...slug]]"
  );
  fs.mkdirSync(routeDir, { recursive: true });

  const routeContent = `// Auto-generated by patch-upstream-workbench.cjs
// Re-exports the workflow entrypoint as a Restate catch-all route

// Need this to register steps!
// noinspection ES6UnusedImports
import "../../.well-known/workflow/v1/step/route.js";

import { POST as wf } from "../../.well-known/workflow/v1/flow/route.js";

export const POST = wf;
export const GET = wf;
`;

  fs.writeFileSync(path.join(routeDir, "route.js"), routeContent);
  console.log(`Created ${path.relative(appDir, routeDir)}/route.js`);
}

console.log("Done — workbench is patched for Restate");
